print("Logic operator example")
n1 = input("enter the first number:")
n2 = input("enter the second number:")
n1 = int(n1)
n2 = int(n2)

if n1 > n2:
    print(f"""{n1} is bigger than {n2}""")
elif n1 < n2:
    print(f"""{n1} is smaller than {n2}""")
else:
    print(f"""{n1} is equal than {n2}""")

